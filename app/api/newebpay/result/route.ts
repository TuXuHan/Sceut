import { NextRequest, NextResponse } from "next/server"
import { createClient } from "@supabase/supabase-js"
import { parsePeriodicPaymentResponse } from "@/lib/newebpay"
import { parseOrderMetadata, serializeOrderMetadata } from "@/lib/order-metadata"

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL!,
  process.env.SUPABASE_SERVICE_ROLE_KEY!,
)

function parseNewebPayDate(dateStr?: string) {
  if (!dateStr) return null

  if (/^\d{8}$/.test(dateStr)) {
    return new Date(`${dateStr.slice(0, 4)}-${dateStr.slice(4, 6)}-${dateStr.slice(6, 8)}`)
  }

  if (/^\d{14}$/.test(dateStr)) {
    return new Date(
      `${dateStr.slice(0, 4)}-${dateStr.slice(4, 6)}-${dateStr.slice(6, 8)}T` +
      `${dateStr.slice(8, 10)}:${dateStr.slice(10, 12)}:${dateStr.slice(12, 14)}`,
    )
  }

  const parsed = new Date(dateStr)
  return Number.isNaN(parsed.getTime()) ? null : parsed
}

function buildBaseUrl(request: NextRequest) {
  const protocol = request.headers.get("x-forwarded-proto") || "http"
  const host = request.headers.get("x-forwarded-host") || request.headers.get("host") || "localhost:3000"
  return `${protocol}://${host}`
}

function buildRedirectHtml(url: string, message: string) {
  return new NextResponse(
    `<!DOCTYPE html>
    <html>
      <head>
        <meta charset="utf-8">
        <title>${message}</title>
      </head>
      <body>
        <script>window.location.href = "${url}";</script>
        <p>${message}</p>
      </body>
    </html>`,
    {
      status: 200,
      headers: {
        "Content-Type": "text/html",
      },
    },
  )
}

async function parseRequestPayload(request: NextRequest) {
  try {
    const formData = await request.formData()
    return Object.fromEntries(formData.entries())
  } catch {
    const text = await request.text()
    const params = new URLSearchParams(text)
    return Object.fromEntries(params.entries())
  }
}

async function syncConfirmedOrder(params: {
  merchantOrderNo: string
  periodNo: string
  authDate?: string
  authTime?: string
  authCode?: string
  respondCode?: string
  nextAuthDate?: string
}) {
  const { merchantOrderNo, periodNo, authDate, authTime, authCode, respondCode, nextAuthDate } = params
  if (!merchantOrderNo || !periodNo) {
    return { matchedOrderId: null }
  }

  const { data: candidateOrders, error: orderFetchError } = await supabase
    .from("orders")
    .select("*")
    .ilike("notes", `%${merchantOrderNo}%`)
    .order("created_at", { ascending: false })
    .limit(20)

  if (orderFetchError) {
    throw orderFetchError
  }

  const matchedOrder = (candidateOrders || []).find((order) => {
    const metadata = parseOrderMetadata(order.notes)
    return (
      metadata.payment?.merchantOrderNo === merchantOrderNo &&
      (metadata.payment?.periodNo === periodNo || metadata.autoGenerated?.targetPeriodNo === periodNo)
    )
  })

  if (!matchedOrder) {
    return { matchedOrderId: null }
  }

  const now = new Date().toISOString()
  const paidAt =
    parseNewebPayDate(authTime || authDate)?.toISOString() ||
    parseNewebPayDate(authDate)?.toISOString() ||
    now

  const updatedNotes = serializeOrderMetadata(matchedOrder.notes, {
    payment: {
      merchantOrderNo,
      periodNo,
      status: "confirmed",
      authTime: authTime || "",
      authDate: authDate || "",
      authCode: authCode || "",
      respondCode: respondCode || "",
      nextAuthDate: nextAuthDate || "",
      confirmedAt: now,
    },
  })

  const { error: orderUpdateError } = await supabase
    .from("orders")
    .update({
      order_status: "confirmed",
      paid_at: paidAt,
      updated_at: now,
      notes: updatedNotes,
    })
    .eq("id", matchedOrder.id)

  if (orderUpdateError) {
    throw orderUpdateError
  }

  const subscriptionId = parseOrderMetadata(updatedNotes).autoGenerated?.subscriptionId
  if (subscriptionId) {
    const { data: subscription } = await supabase
      .from("subscribers")
      .select("payment_data")
      .eq("id", subscriptionId)
      .maybeSingle()

    const existingPaymentData =
      subscription?.payment_data && typeof subscription.payment_data === "object"
        ? subscription.payment_data
        : {}

    const nextPaymentDate = parseNewebPayDate(nextAuthDate)?.toISOString()

    const { error: subscriptionUpdateError } = await supabase
      .from("subscribers")
      .update({
        updated_at: now,
        last_payment_date: paidAt,
        next_payment_date: nextPaymentDate || undefined,
        payment_status: "paid",
        payment_data: {
          ...existingPaymentData,
          MerchantOrderNo: merchantOrderNo,
          PeriodNo: periodNo,
          AuthTime: authTime || existingPaymentData.AuthTime,
          AuthDate: authDate || existingPaymentData.AuthDate,
          AuthCode: authCode || existingPaymentData.AuthCode,
          RespondCode: respondCode || existingPaymentData.RespondCode,
          NextAuthDate: nextAuthDate || existingPaymentData.NextAuthDate,
          already_times: Number(periodNo),
          last_confirmed_at: now,
        },
      })
      .eq("id", subscriptionId)

    if (subscriptionUpdateError) {
      throw subscriptionUpdateError
    }
  }

  return { matchedOrderId: matchedOrder.id }
}

export async function POST(request: NextRequest) {
  try {
    const responseData = await parseRequestPayload(request)
    const topLevelStatus = String(responseData.Status || responseData.status || "")
    const topLevelMessage = String(responseData.Message || responseData.message || "")
    const baseUrl = buildBaseUrl(request)

    if (topLevelStatus && topLevelStatus !== "SUCCESS" && topLevelStatus.toLowerCase() !== "success") {
      const errorUrl = `${baseUrl}/test-newebpay-config?error=${encodeURIComponent(topLevelMessage || topLevelStatus)}&status=${encodeURIComponent(topLevelStatus)}`
      return buildRedirectHtml(errorUrl, `付款失敗：${topLevelMessage || topLevelStatus}`)
    }

    const encryptedPeriod = responseData.Period || responseData.period
    if (!encryptedPeriod || typeof encryptedPeriod !== "string") {
      return NextResponse.json(
        {
          error: "Period field not found in NeWebPay response",
          receivedData: responseData,
        },
        { status: 400 },
      )
    }

    const parsedResponse = parsePeriodicPaymentResponse(encryptedPeriod)
    const result = parsedResponse.Result || {}
    const merchantOrderNo = String(result.MerchantOrderNo || "")
    const periodNo = String(result.PeriodNo || "")
    const authTime = String(result.AuthTime || "")
    const authDate = String(result.AuthDate || "")
    const authCode = String(result.AuthCode || "")
    const respondCode = String(result.RespondCode || "")
    const nextAuthDate = String(result.NextAuthDate || "")
    const periodAmt = String(result.PeriodAmt || "")

    const isConfirmed =
      parsedResponse.Status === "SUCCESS" &&
      (!respondCode || respondCode === "00")

    if (isConfirmed) {
      await syncConfirmedOrder({
        merchantOrderNo,
        periodNo,
        authDate,
        authTime,
        authCode,
        respondCode,
        nextAuthDate,
      })

      const redirectUrl =
        `${baseUrl}/subscribe/success?` +
        `PeriodNo=${encodeURIComponent(periodNo)}` +
        `&AuthTime=${encodeURIComponent(authTime || authDate)}` +
        `&PeriodAmt=${encodeURIComponent(periodAmt)}` +
        `&MerchantOrderNo=${encodeURIComponent(merchantOrderNo)}`

      return buildRedirectHtml(redirectUrl, "付款確認中，正在跳轉...")
    }

    return NextResponse.json(
      {
        status: "error",
        message: parsedResponse.Message || "Payment failed",
        details: parsedResponse,
      },
      { status: 400 },
    )
  } catch (error) {
    console.error("❌ Error processing payment result POST:", error)
    return NextResponse.json(
      {
        error: "Failed to process payment result",
        message: error instanceof Error ? error.message : "Unknown error occurred",
      },
      { status: 500 },
    )
  }
}

export async function GET() {
  return NextResponse.json({
    message: "NeWebPay Payment Result API",
    endpoints: {
      POST: "Handle payment result from NeWebPay",
      GET: "Get API information",
    },
    note: "This endpoint receives form data from NeWebPay after payment processing",
  })
}
